#!/bin/bash

# Remember to type in terminal before first execution: chmod 755 main.sh 
# To execute: ./main.sh
# if error "bad interpreter: No such file or directory" --> sed -i -e 's/\r$//' main.sh

# ================================================================================================================================ #
# ============================================================ DEFINITIONS ======================================================= #
# ================================================================================================================================ #

nc='\033[0m'
red='\033[0;31m'
blue='\033[0;34m'
green='\033[0;32m'

ANACONDA3="python3"

# ================================================================================================================================ #
# =========================================================== C COMPILATION ====================================================== #
# ================================================================================================================================ # 

echo
echo -e "${blue}Initilizing compilation${nc}"
make
echo -e "${green}Compilation finished${nc}"
echo

# ================================================================================================================================ #
# =========================================================== C execution ======================================================== #
# ================================================================================================================================ # 

filename0_c_bin="main.bin"

echo
echo -e "${blue}Executing ${filename0_c_bin}${nc}"
./${filename0_c_bin}
echo -e "${green}Execution of ${filename0_c_bin} finished${nc}"
echo

# ================================================================================================================================ #
# ======================================================== python execution ====================================================== #
# ================================================================================================================================ #

filename_py="plot_tool.py"

echo
echo -e "${blue}Executing ${filename_py}${nc}"
${ANACONDA3} ${filename_py}
echo -e "${green}Execution of ${filename_py} finished${nc}"
echo
